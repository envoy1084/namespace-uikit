const sourceCss = await readFile(sourcePath, "utf8");
const lightTokens = toHexTokens(readTokens(extractBlock(sourceCss, ":root")));
const darkTokens = toHexTokens(readTokens(extractBlock(sourceCss, ".dark")));

const colors = {
  zinc,
  accent: {
    DEFAULT: lightTokens.accent,
    foreground: lightTokens.accentForeground,
  },
  light: lightTokens,
  dark: darkTokens,
};

