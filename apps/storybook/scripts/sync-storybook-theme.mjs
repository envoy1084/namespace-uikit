const toObjectKey = (tokenName) =>
  tokenName.replace(/-([a-z])/g, (_, letter) => letter.toUpperCase());

const serializeObject = (value, indent = 0) => {
  const pad = " ".repeat(indent);
  const childPad = " ".repeat(indent + 2);

  if (typeof value === "string") {
    return JSON.stringify(value);
  }

  const entries = Object.entries(value)
    .map(([key, child]) => {
      const property = /^[a-zA-Z_$][\w$]*$/.test(key)
        ? key
        : JSON.stringify(key);

      return `${childPad}${property}: ${serializeObject(child, indent + 2)},`;
    })
    .join("\n");

  return `{\n${entries}\n${pad}}`;
};

const extractBlock = (css, selectorStart) => {
  const start = css.indexOf(selectorStart);

  if (start === -1) {
    throw new Error(`Could not find selector block: ${selectorStart}`);
  }

  const open = css.indexOf("{", start);

  if (open === -1) {
    throw new Error(`Could not find opening brace for: ${selectorStart}`);
  }

  let depth = 0;

  for (let index = open; index < css.length; index += 1) {
    const char = css[index];

    if (char === "{") depth += 1;
    if (char === "}") depth -= 1;

    if (depth === 0) {
      return css.slice(open + 1, index);
    }
  }

  throw new Error(`Could not find closing brace for: ${selectorStart}`);
};

const readTokens = (block) => {
  const tokens = {};

  for (const tokenName of tokenNames) {
    const pattern = new RegExp(`--${tokenName}:\\s*([^;]+);`);
    const match = block.match(pattern);

    if (!match) {
      throw new Error(`Missing token: --${tokenName}`);
    }

    const raw = match[1].trim();

    tokens[toObjectKey(tokenName)] = raw;
  }

  return tokens;
};

const toHex = (value) => {
  if (value === "transparent") return "transparent";

  const color = parse(value);

  if (!color) {
    throw new Error(`Unable to parse color: ${value}`);
  }

  return formatHex(color).toUpperCase();
};

const toHexTokens = (tokens) =>
  Object.fromEntries(
    Object.entries(tokens).map(([key, value]) => [key, toHex(value)]),
  );

