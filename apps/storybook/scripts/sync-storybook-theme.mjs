import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { formatHex, parse } from "culori";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const storybookRoot = resolve(scriptDir, "..");
const workspaceRoot = resolve(storybookRoot, "../..");
const uikitThemePath = resolve(
  workspaceRoot,
  "packages/uikit/src/styles/globals.css",
);
const storybookThemePath = resolve(storybookRoot, ".storybook/styles/theme.ts");

const lightSelector =
  ':root,\\s*\\.light,\\s*\\.default,\\s*\\[data-theme="light"\\],\\s*\\[data-theme="default"\\]';
const darkSelector = '\\.dark,\\s*\\[data-theme="dark"\\]';

const css = readFileSync(uikitThemePath, "utf8");

function extractBlock(selector, label) {
  const blockRegex = new RegExp(`${selector}\\s*\\{([\\s\\S]*?)\\n\\}`, "m");
  const match = css.match(blockRegex);

  if (!match) {
    throw new Error(`Could not find ${label} theme block in ${uikitThemePath}`);
  }

  return match[1];
}

function extractVariables(block) {
  const variables = {};
  const variableRegex = /--([a-z0-9-]+):\s*([^;]+);/gi;

  for (const match of block.matchAll(variableRegex)) {
    variables[match[1]] = match[2].trim();
  }

  return variables;
}

function getVariable(variables, name, mode) {
  const value = variables[name];

  if (!value) {
    throw new Error(`Missing --${name} in ${mode} theme block`);
  }

  return value;
}

function toThemeColor(value) {
  if (value === "transparent") {
    return value;
  }

  const color = parse(value);

  if (!color) {
    throw new Error(`Could not parse color value "${value}"`);
  }

  return formatHex(color).toUpperCase();
}

function toPxNumber(value) {
  const trimmedValue = value.trim();

  if (trimmedValue.endsWith("rem")) {
    return Number.parseFloat(trimmedValue) * 16;
  }

  if (trimmedValue.endsWith("px")) {
    return Number.parseFloat(trimmedValue);
  }

  const parsedValue = Number.parseFloat(trimmedValue);

  if (Number.isNaN(parsedValue)) {
    throw new Error(`Could not parse radius value "${value}"`);
  }

  return parsedValue;
}

function buildModeTheme(variables, mode) {
  const border = toThemeColor(getVariable(variables, "border", mode));
  const fieldBorder = toThemeColor(
    getVariable(variables, "field-border", mode),
  );

  return {
    background: toThemeColor(getVariable(variables, "background", mode)),
    border,
    fieldBackground: toThemeColor(
      getVariable(variables, "field-background", mode),
    ),
    fieldBorder: fieldBorder === "transparent" ? border : fieldBorder,
    fieldForeground: toThemeColor(
      getVariable(variables, "field-foreground", mode),
    ),
    foreground: toThemeColor(getVariable(variables, "foreground", mode)),
    muted: toThemeColor(getVariable(variables, "muted", mode)),
  };
}

function buildThemeConfig() {
  const lightVariables = extractVariables(extractBlock(lightSelector, "light"));
  const darkVariables = extractVariables(extractBlock(darkSelector, "dark"));

  return {
    accent: {
      DEFAULT: toThemeColor(getVariable(lightVariables, "accent", "light")),
      foreground: toThemeColor(
        getVariable(lightVariables, "accent-foreground", "light"),
      ),
    },
    appBorderRadius: toPxNumber(getVariable(lightVariables, "radius", "light")),
    inputBorderRadius: toPxNumber(
      getVariable(lightVariables, "field-radius", "light"),
    ),
    light: buildModeTheme(lightVariables, "light"),
    dark: buildModeTheme(darkVariables, "dark"),
  };
}

